// Fisher-Yates shuffle for proper randomization
const shuffleArray = (array) => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

export function getQuestionsForState(state) {
  const questions = questionBank[state] || [];
  if (questions.length === 0) {
    console.warn(`No questions found for state: ${state}`);
    return [];
  }
  return questions;
}

export function getQuestionsByTopic(state, topic) {
  const allQuestions = getQuestionsForState(state);
  if (topic === 'All Topics') {
    return allQuestions;
  }
  return allQuestions.filter(q => q.topic === topic);
}

export function getRandomQuestions(state, count, topic = 'All Topics', excludeMath = false) {
  let questions = getQuestionsByTopic(state, topic);
  
  // Filter out Auction Math questions if requested (for timed games)
  if (excludeMath) {
    questions = questions.filter(q => q.topic !== 'Auction Math');
  }
  
  if (questions.length === 0) {
    console.warn(`No questions available for state: ${state}, topic: ${topic}`);
    return [];
  }
  
  // Shuffle questions
  const shuffled = shuffleArray(questions);
  
  // If we need more questions than available, repeat the shuffled array
  if (count > shuffled.length) {
    const repetitions = Math.ceil(count / shuffled.length);
    const repeated = [];
    for (let i = 0; i < repetitions; i++) {
      repeated.push(...shuffleArray(questions));
    }
    return repeated.slice(0, count);
  }
  
  return shuffled.slice(0, count);
}

export function getQuizQuestions(state, count, topic = 'All Topics') {
  return getRandomQuestions(state, count, topic);
}

export function getAvailableTopics(state) {
  const stateQuestions = getQuestionsForState(state);
  const topicsWithQuestions = new Set();
  
  stateQuestions.forEach(q => {
    if (q.topic) {
      topicsWithQuestions.add(q.topic);
    }
  });
  
  return ['All Topics', ...Array.from(topicsWithQuestions).sort()];
}
